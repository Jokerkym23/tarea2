# Pide el nombre completo del usuario
nombre_completo = input("Escribe tu nombre completo como quieras  ")

# Convierte el nombre completo en minusculas
nombre_completo_minusculas = nombre_completo.lower()

# Convierte todo el nombre en mayusculas 
nombre_completo_mayusculas = nombre_completo.upper()

# inicial mayuscula
nombre_completo_primeras_letras_mayusculas = nombre_completo.title()

# Imprime los nombres 
print(f"Tu nombre completo en minúsculas es: {nombre_completo_minusculas}")
print(f"Tu nombre completo en mayúsculas es: {nombre_completo_mayusculas}")
print(f"Tu nombre completo con inical mayuscula es: {nombre_completo_primeras_letras_mayusculas}")
